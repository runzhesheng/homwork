#include<iostream>
#include<cmath>
using namespace std;
int main()
{
    cout<<"ceil(8.2)= "  <<ceil(8.2)<<endl;             // 取整为不小于x的最小整数
    cout<<"ceil(-8.9)= " <<ceil(-8.9)<<endl;
    cout<<"cos(60)= "    <<cos(60)<<endl;               //（弧度）的三角余弦
    cout<<"exp(1)= "     <<exp(1)<<endl;                // 指数函数
    cout<<"fabs(-20)= "  <<fabs(-20)<<endl;             // 取绝对值
    cout<<"floor(6.7)= " <<floor(6.7)<<endl;            // 取整为不大于x的最大整数
    cout<<"floor(-6.2)= "<<floor(-6.2)<<endl;
    cout<<"fmod(3.3,1)= "<<fmod(3.3,1)<<endl;           // x/y的浮点数余数
    cout<<"log(e)= "     <<log(2.718282)<<endl;         // 自然对数 ln(X)
    cout<<"log10(10)= "  <<log10(10)<<endl;
    cout<<"pow(2,2)= "   <<pow(2,2)<<endl;              // x^y=pow(x,y)
    cout<<"sin(30)= "    <<sin(30)<<endl;               //（弧度）的三角正弦
    cout<<"sqrt(9)= "    <<sqrt(9)<<endl;               // 非负数的平方根
    cout<<"tan(45)= "    <<tan(45)<<endl;               //（弧度）的三角正切
    return 0;
}
